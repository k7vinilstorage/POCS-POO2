    private void selPesquisaCbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selPesquisaCbActionPerformed
        int op = selPesquisaCb.getSelectedIndex();
        DefaultTableModel model = (DefaultTableModel) resultTb.getModel();
        switch(op) {
            case 0:
                model.setColumnIdentifiers(new String [] {"Nome", "Matéria", "Aulas dadas"});
                break;
            case 1:
                model.setColumnIdentifiers(new String [] {"Nome", "Matéria", "Aulas vistas"});
                break;
            case 2:
                model.setColumnIdentifiers(new String [] {"Professor", "Aluno", "Horário"});
                break;
        }
    }//GEN-LAST:event_selPesquisaCbActionPerformed


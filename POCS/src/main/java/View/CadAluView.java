    private boolean getAluno(){
        Utils verifica = Utils.createUtils();
        Aluno aln = new Aluno();
        aln.setNome(nomeTf.getText());
        aln.setSenha(senhaTf.getText());
        aln.setCpf(cpfTf.getText());
        aln.setEmail(emailTf.getText());
        aln.setCelular(celTf.getText());
        aln.setEscola(escolaTf.getText());
        
        
        
        AlunoCtrl alnCtrl = AlunoCtrl.AlunoCtrlCreate();
        
        if(verifica.validarCpf(cpfTf.getText()) || verifica.verificarEmail(emailTf.getText())){
            if(verifica.verificarCpfDuplicado(cpfTf.getText())){
                alnCtrl.inserirTabela(aln);
                DialogsView.createDialogs().infoDialog("Aluno cadastrado com sucesso", "CADASTRO");
                return true;
            }else{
               JOptionPane.showMessageDialog(
                    null,
                    "CPF ja cadastrado",
                    "INFORMAÇÃO",
                    JOptionPane.INFORMATION_MESSAGE
                ); 
               return false;
            }            
        }else{
            JOptionPane.showMessageDialog(
                    null,
                    "Email ou CPF incorretos",
                    "INFORMAÇÃO",
                    JOptionPane.INFORMATION_MESSAGE
            );
            return false;
        }
    }
    

    private boolean getProfessor(){
        Utils verifica = Utils.createUtils();
        Professor prof = new Professor();
        prof.setNome(nomeTf.getText());//nome
        prof.setSenha(senhaTf.getText());//senha
        prof.setCpf(cpfTf.getText());//cpf
        prof.setEmail(emailTf.getText());//email
        prof.setCelular(celTf.getText());//celular
        prof.setDisciplina(disciplinaTf.getText());//disciplina
        prof.setFormacao(formacaoTf.getText());//formacao
    
        ProfessorCtrl profCtrl = ProfessorCtrl.ProfessorCtrlCreate();
        
        if(verifica.validarCpf(cpfTf.getText()) || verifica.verificarEmail(emailTf.getText())){
          if(verifica.verificarCpfDuplicado(cpfTf.getText())){
                profCtrl.inserirTabela(prof);
                DialogsView.createDialogs().infoDialog("Professor cadastrado com sucesso!", "CADASTRO");
                return true;
            }else{
               JOptionPane.showMessageDialog(
                    null,
                    "CPF ja cadastrado",
                    "INFORMAÇÃO",
                    JOptionPane.INFORMATION_MESSAGE
                ); 
               return false;
            }            
        }else{
            JOptionPane.showMessageDialog(
                    null,
                    "Email ou CPF incorretos",
                    "INFORMAÇÃO",
                    JOptionPane.INFORMATION_MESSAGE
            );
            return false;
        }
    
    }
    
